def calculate_effectiveness(type_str):
    """Returns a dict of all 18 types and their multiplier against the given type_str."""
    f_types = type_str.split('/')
    all_types = list(TYPE_CHART.keys())
    results = {t: 1.0 for t in all_types}
    
    for attacker in all_types:
        for defender in f_types:
            multiplier = TYPE_CHART.get(attacker, {}).get(defender, 1.0)
            results[attacker] *= multiplier
            
    return results